package com.norswap.autumn.parsing;

public class ParseError
{
    ////////////////////////////////////////////////////////////////////////////////////////////////

    private final ParsingExpression failedExpression;

    private final int position;

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public ParseError(ParsingExpression failedExpression, int position)
    {
        this.failedExpression = failedExpression;
        this.position = position;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public ParsingExpression failedExpression()
    {
        return failedExpression;
    }

    public int position()
    {
        return position;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
}
