package com.norswap.autumn.debugger;

import com.norswap.autumn.debugger.locators.CaptureLocator;
import com.norswap.util.Array;

/**
 *
 */
public final class CaptureInfo
{
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public final Array<CaptureLocator> captures;

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public CaptureInfo(Array<CaptureLocator> captures)
    {
        this.captures = captures;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    CaptureLocator capturingCapture()
    {
        return captures.last();
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
}
