package com.norswap.util;

/**
 * A simple modifiable counter for use inside forEach loops and the such.
 * <p>
 * e.g.:
 * <pre>
 *      i c = i.i();
 *      Arrays.stream(array).forEach(x -> c.i++);
 * </pre>
 */
public class Counter
{
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public int i;

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public Counter() {}

    // ---------------------------------------------------------------------------------------------

    public Counter(int j)
    {
        i = j;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public static Counter counter()
    {
        return new Counter();
    }

    // ---------------------------------------------------------------------------------------------

    public static Counter counter(int j)
    {
        return new Counter(j);
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
}
